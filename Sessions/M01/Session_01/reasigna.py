# programa para operadores de reasignacion y entrada de datos

x = int(input('Ingrese valor: '))

x += 2
print('Evaluando += el nuevo valor es: ', x)

# Ejercicio

# *=
x *= 2
print('Evaluando *= el nuevo valor es: ', x)

# /=
x /= 2
print('Evaluando /= el nuevo valor es: ', x)

# -=
x -= 2
print('Evaluando -= el nuevo valor es: ', x)

# //=
x //= 2
print('Evaluando //= el nuevo valor es: ', x)

# **=
x **= 2
print('Evaluando **= el nuevo valor es: ', x)

# %=
x %= 2
print('Evaluando %= el nuevo valor es: ', x)
